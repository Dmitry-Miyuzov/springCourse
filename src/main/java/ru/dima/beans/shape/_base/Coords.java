package ru.dima.beans.shape._base;

import org.springframework.stereotype.Component;

@Component
public class Coords {
    private Double x;
    private Double y;

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
